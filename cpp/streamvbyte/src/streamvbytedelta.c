			Data = _decode_avx((keys & 0x00FF), &dataPtr);
			Prev = _write_avx_d1(out + 24, Data, Prev);
			Data = _decode_avx((keys & 0xFF00) >> 8, &dataPtr);
			Prev = _write_avx_d1(out + 28, Data, Prev);

